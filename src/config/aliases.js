let __aliases = null;

export function getAliases(o) {
  if (!__aliases && o) {
    const net = isMainnet(o.chain_id) ? undefined : o.chain_id;
    __aliases = {};
    Object.keys(allAliases).forEach(key => {
      if (allAliases[key].net === net) {
        __aliases[key] = allAliases[key];
      }
    });
  }
  return __aliases || allAliases;
}

let payoutMap = null;

export function getPayoutAddresses(baker = []) {
  // lazy-create payout map
  if (!payoutMap) {
    payoutMap = new Map();
    const aliases = getAliases();
    Object.keys(aliases).forEach(key => {
      const desc = aliases[key];
      if (desc.from) {
        let arr = payoutMap.get(desc.from)||[];
        arr.push(key);
        payoutMap.set(desc.from, arr);
      }
    });
  }
  // ensure we handle an array as input
  baker = Array.isArray(baker)?baker:[baker];
  if (!baker.length) {
    return Array.from(payoutMap.values()).flat();
  }
  let payouts = [];
  baker.forEach(b => {
    payouts.push(b);                         // add the baker address
    payouts.push(...(payoutMap.get(b)||[])); // add payout addresses
  });
  return payouts
}